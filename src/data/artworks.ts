function getArtworkOrientation(slug: string, formatCategory: ArtworkFormatCategory): Artwork["orientation"] {
  if (landscapeArtworkSlugs.has(slug) || formatCategory === "landscape" || formatCategory === "wide") {
    return "landscape";
  }
  if (formatCategory === "square") return "square";
  return "portrait";
}

function withImagePaths(record: ArtworkRecord): Artwork {
  const hasWideArtwork = wideArtworkSlugs.has(record.slug);
  const metadata = artworkImageMetadata[record.slug];

  if (!metadata) {
    throw new Error(`Missing image metadata for ${record.slug}`);
  }

  return {
    ...record,
    src: `/images/artworks/full/${record.slug}.jpg`,
    thumbnail: `/images/artworks/thumbs/${record.slug}.jpg`,
    ...(hasWideArtwork
      ? {
          wideSrc: `/images/artworks/wide/${record.slug}.jpg`,
          wideThumbnail: `/images/artworks/wide-thumbs/${record.slug}.jpg`,
        }
      : {}),
    width: metadata.width,
    height: metadata.height,
    aspectRatio: metadata.aspectRatio,
    formatCategory: metadata.formatCategory,
    orientation: getArtworkOrientation(record.slug, metadata.formatCategory),
  };
}

